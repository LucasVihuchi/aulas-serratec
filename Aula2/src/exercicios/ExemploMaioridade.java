package exercicios;

public class ExemploMaioridade {

	public static void main(String[] args) {
		int idade = 18;
		
		
		if (idade == 18) {
			System.out.println("A pessoa tem 18 anos");
		}
		else if (idade > 18) {
			System.out.println("A pessoa tem mais de 18 anos");
		}
		else {
			System.out.println("A pessoa tem menos de 18 anos");
		}

	}

}
